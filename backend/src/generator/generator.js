/**
 * Main generator function
 * @param {object} options - Command-line options
 */
async function generate(options) {
  try {
    // Load schema
    console.log('Loading JSON schema...');
    const schema = loadSchema();

    // Load and parse config
    console.log(`Reading config: ${options.config}`);
    if (!fs.existsSync(options.config)) {
      throw new Error(`Config file not found: ${options.config}`);
    }

    const configContent = fs.readFileSync(options.config, 'utf8');
    let config;
    try {
      config = JSON.parse(configContent);
    } catch (err) {
      throw new Error(`Invalid JSON in config: ${err.message}`);
    }

    // Validate config
    console.log('Validating config against schema...');
    const validation = validateConfig(config, schema);
    if (!validation.valid) {
      console.error('Validation failed:');
      validation.errors.forEach(err => {
        console.error(`  ${err.path}: ${err.message}`);
      });
      process.exit(1);
    }
    console.log('✓ Config valid');

    // If --validate-only, stop here
    if (options['validate-only']) {
      console.log('✓ Validation complete (--validate-only flag set)');
      process.exit(0);
    }

    // Create output directory
    const outputDir = options.output || `./generated-${config.name}`;
    if (!fs.existsSync(outputDir)) {
      fs.mkdirSync(outputDir, { recursive: true });
      console.log(`✓ Created output directory: ${outputDir}`);
    }

    // Generate .env file
    console.log('Generating .env.local with injected parameters...');
    generateEnvFile(config, outputDir);

    // Generate deployment guide
    console.log('Generating DEPLOYMENT-GUIDE.md...');
    generateDeploymentGuide(config, outputDir);

    // Phase 2+ will add:
    // - Contract template copy + parameter injection
    // - Subgraph generation
    // - Docker Compose setup

    console.log(`\n✓ Fork generation complete!`);
    console.log(`\nNext steps:`);
    console.log(`  1. Review .env.local: ${path.join(outputDir, '.env.local')}`);
    console.log(`  2. Copy contracts and backend code`);
    console.log(`  3. Deploy: npm run deploy:testnet`);
    console.log(`  4. See DEPLOYMENT-GUIDE.md for full instructions`);

    process.exit(0);
  } catch (error) {
    console.error('Error:', error.message);
    process.exit(1);
  }
}

// CLI entry point
if (require.main === module) {
  const yargs = require('yargs');
  const argv = yargs
    .option('config', {
      alias: 'c',
      description: 'Path to country configuration JSON file',
      type: 'string',
    })
    .option('output', {
      alias: 'o',
      description: 'Output directory for generated fork',
      type: 'string',
    })
    .option('validate-only', {
      description: 'Validate config without generating',
      type: 'boolean',
      default: false,
    })
    .option('show-schema', {
      description: 'Print JSON schema and exit',
      type: 'boolean',
      default: false,
    })
    .option('list-configs', {
      description: 'List available country configs',
      type: 'boolean',
      default: false,
    })
    .demandOption(['config'], 'Please provide --config with path to country configuration JSON')
    .help()
    .argv;

  if (argv['show-schema']) {
    const schema = loadSchema();
    console.log(JSON.stringify(schema, null, 2));
    process.exit(0);
  }

  generate(argv);
}

module.exports = { generate, validateConfig, loadSchema, generateEnvFile, generateDeploymentGuide };
