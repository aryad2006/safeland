/**
 * SafeLand Fork Generator
 *
 * Main generator orchestrator:
 * 1. Validates country config against JSON schema
 * 2. Generates .env files with injected parameters
 * 3. Copies contract templates and injects parameters
 * 4. Generates deployment documentation
 * 5. Outputs complete fork ready for deployment
 *
 * Usage:
 *   node generator.js --config countries/senegal.json --output ../safeland-senegal
 *
 * Phase 1 MVP (Apr-May 2026):
 * - JSON schema validation only
 * - .env file generation with parameter injection
 * - Basic template copy (no Solidity templating yet)
 * - Documentation generation
 *
 * Phase 2+ enhancements:
 * - Solidity contract parameter injection
 * - Interactive CLI wizard
 * - Subgraph generation
 * - CI/CD integration
 */

const fs = require('fs');
const path = require('path');
const Ajv = require('ajv'); // npm install ajv

/**
 * Load and validate JSON schema
 * @returns {object} Parsed schema
 */
function loadSchema() {
  const schemaPath = path.join(__dirname, 'schema.json');
  const schemaContent = fs.readFileSync(schemaPath, 'utf8');
  return JSON.parse(schemaContent);
}

/**
 * Validate country config against schema
 * @param {object} config - Country configuration
 * @param {object} schema - JSON schema
 * @returns {object} { valid: boolean, errors: [] }
 */
function validateConfig(config, schema) {
  const ajv = new Ajv();
  const validate = ajv.compile(schema);
  const valid = validate(config);

  return {
    valid,
    errors: valid ? [] : validate.errors.map(err => ({
      path: err.instancePath || 'root',
      message: err.message,
    })),
  };
}

